    (児童生徒宛名番号,児童生徒氏名カナ,児童生徒氏名漢字,世帯番号,世帯員宛名番号,
--    世帯員氏名かな,世帯員氏名漢字,人格区分,生年月日,表示用生年月日,
    世帯員氏名カナ,世帯員氏名漢字,人格区分,生年月日,表示用生年月日,
