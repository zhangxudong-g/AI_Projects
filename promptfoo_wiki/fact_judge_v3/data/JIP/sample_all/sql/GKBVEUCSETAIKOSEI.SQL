    性別,現住所,続柄コード１,続柄コード２,続柄コード３,続柄コード４,
    続柄名,筆頭者氏名)
AS
SELECT
     GAKUREIBO.KOJIN_NO
    ,ATENAJ.SHIMEI_KANA
    ,ATENAJ.SHIMEI_KANJI
    ,ATENAJ.SETAI_NO
    ,ATENA.KOJIN_NO
    ,ATENA.SHIMEI_KANA
    ,ATENA.SHIMEI_KANJI
    ,ATENA.JINKAKU_KBN
    ,ATENA.SEINENGAPI
-- 2020/06/11 JIP.Makishima Mod 5.4.000.000: UAT故障対応(表示生年月日に修正)
--    ,ATENA.WAREKI_SEINENGAPI
    ,ATENA.HYOJI_SEINENGAPI
    ,DECODE(ATENA.SEIBETSU,1,'男',2,'女','') SEIBETSU
    ,GAAPK0010.FADDRESSEDIT(1,'',ATENA.CHOMEI,ATENA.BANCHI,ATENA.KATAGAKI,'')
    ,ATENA.ZOKUGARA_CD1
    ,ATENA.ZOKUGARA_CD2
    ,ATENA.ZOKUGARA_CD3
    ,ATENA.ZOKUGARA_CD4
    ,ATENA.ZOKUGARA_MEI
    ,JUKIJUSHO.NUSHI_HITTOSYA
FROM GKBTGAKUREIBO  GAKUREIBO
    ,GABTATENAKIHON KIHON
    ,GABVATENAALL   ATENA
    ,GABVATENAALL   ATENAJ
    ,GABTJUKIJUSHO  JUKIJUSHO
WHERE
    GAKUREIBO.KOJIN_NO = KIHON.KOJIN_NO
AND GAKUREIBO.KOJIN_NO = ATENAJ.KOJIN_NO
AND KIHON.SETAI_NO = ATENA.SETAI_NO
--2020/03/05 DIS.yangwzh update start 5.4.000.000:設計書変更の対応：結合条件の変更
--AND ATENA.KOJIN_NO = JUKIJUSHO.KOJIN_NO
--AND ATENA.HONSEKI_REN = JUKIJUSHO.JUSHO_REN
AND ATENA.KOJIN_NO = JUKIJUSHO.KOJIN_NO(+)
AND ATENA.HONSEKI_REN = JUKIJUSHO.JUSHO_REN(+)
--2020/03/05 DIS.yangwzh update end
AND GAKUREIBO.GAKUNEN = 0
AND GAKUREIBO.GEN_JIYU_CD = 0
AND GAKUREIBO.GEN_NENGAPI = 0
AND GAKUREIBO.SAISHIN_KBN = 0
AND ATENA.GENZON_KBN = 0
AND ATENA.RIREKI_RENBAN = (
    SELECT MAX(V1.RIREKI_RENBAN) FROM GABVATENAALL V1 WHERE ATENA.KOJIN_NO = V1.KOJIN_NO GROUP BY V1.KOJIN_NO
)
AND ATENAJ.RIREKI_RENBAN = (
    SELECT MAX(V2.RIREKI_RENBAN) FROM GABVATENAALL V2 WHERE ATENAJ.KOJIN_NO = V2.KOJIN_NO GROUP BY V2.KOJIN_NO
)
ORDER BY GAKUREIBO.GAKKO_KBN_CD ASC,GAKUREIBO.SYOGAKO_CD ASC,GAKUREIBO.TYUGAKO_CD ASC,GAKUREIBO.KUIKIGAI_CD ASC,GAKUREIBO.YOGOGAKO_CD ASC,GAKUREIBO.GAKUNEN ASC,ATENAJ.SHIMEI_KANA ASC,ATENA.KISAI_JUNI ASC
/
