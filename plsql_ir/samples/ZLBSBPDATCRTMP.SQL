    OPEN MKIHON FOR VSQL;
    LOOP
      --データ取り出し
      FETCH MKIHON INTO NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IKOSHINBI;
      EXIT WHEN MKIHON%NOTFOUND;

        IKBN := 0;  --最新データ処理

      IF BRTN THEN
        IR_NO := 0;
        --賦課基本から帳票基本データ(医療全体分)作成
        BRTN := FUNC_FKIHONNGET(IKBN, NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IR_NO);
        EXIT WHEN BRTN = FALSE;
        --戻り値の履歴番号が０：データなしの場合、読み飛ばし
        IF IR_NO > 0 THEN
          --賦課退職から帳票基本データ(医療退職分)作成
          BRTN := FUNC_FTAINGET(IKBN, NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IR_NO);
          EXIT WHEN BRTN = FALSE;

          --賦課介護基本から帳票基本データ(介護全体分)作成
          BRTN := FUNC_FKAIKIHONNGET(IKBN, NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IR_NO);
          EXIT WHEN BRTN = FALSE;

          --賦課介護退職から帳票基本データ(介護退職分)作成
          BRTN := FUNC_FKAITAINGET(IKBN, NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IR_NO);
          EXIT WHEN BRTN = FALSE;
          --賦課支援基本から帳票基本データ(支援全体分)作成
          BRTN := FUNC_FSIENKIHONNGET(IKBN, NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IR_NO);
          EXIT WHEN BRTN = FALSE;
          --賦課支援退職から帳票基本データ(支援退職分)作成
          BRTN := FUNC_FSIENTAINGET(IKBN, NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IR_NO);
          EXIT WHEN BRTN = FALSE;
--2025/08/11 ZCZL.LIUCHENYU ADD START 1.1.200.000:子ども子育て支援金対応
          --賦課子ども基本から帳票基本データ(子ども全体分)作成
          BRTN := FUNC_FKDMKIHONNGET(IKBN, NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IR_NO);
          EXIT WHEN BRTN = FALSE;
--2025/08/11 ZCZL.LIUCHENYU ADD END
        END IF;
      END IF;
      COMMIT;
    END LOOP;
    CLOSE MKIHON;

    IF NOT BRTN THEN
      --エラー対象ログ出力
      VLOG_LOG := '処理対象 国保世帯番号=' || NSETAI_NO || ' 通知書番号=' || NTSUCHI_NO;
      VLOG_LOG := VLOG_LOG || ' 算定団体コード=' || IDANTAI_CD || ' 年度分=' || INENDOBUN;
      PROCWRITELOG(c_NG, VLOG_LOG);
    END IF;

  EXCEPTION
    WHEN OTHERS THEN
      --異常終了
      VLOG_LOG := SQLERRM || ' FUNCMAIN ';
      PROCWRITELOG(c_NG, VLOG_LOG);
  END;
  RETURN(BRTN);
END FUNCMAIN;
