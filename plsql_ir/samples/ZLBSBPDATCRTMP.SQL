-- 帳票基本データ(医療全体分)項目編集
--------------------------------------------------------------------------------
FUNCTION FUNC_FIRYEDIT(i_NMODE IN NUMBER) RETURN BOOLEAN IS
  BRTN     BOOLEAN;                 --リターン値
  CURSOR CKOJIN IS
    SELECT KOJIN_NO, NEW_ZOKU
      FROM ZLBTKOJIN_MID
      WHERE KOKU_SETAI_NO   = RKIHON.KOKU_SETAI_NO
        AND SANTEIDANTAI_CD = RKIHON.SANTEIDANTAI_CD
        AND CHOTEI_NENDO    = RKIHON.CHOTEI_NENDO
        AND NENDO_BUN       = RKIHON.NENDO_BUN
        AND TSUCHI_NO       = RKIHON.TSUCHI_NO
        AND R_NO            = RKIHON.R_NO
    ORDER BY NEW_ZOKU;
BEGIN
  -- 共通項目編集
  BRTN := FUNC_FGETKYOTU(i_NMODE, 1, RKIHON.KOKU_SETAI_NO, RKIHON.SANTEIDANTAI_CD,
                          RKIHON.CHOTEI_NENDO, RKIHON.NENDO_BUN, RKIHON.TSUCHI_NO,
                          RKIHON.R_NO, RKIHON.NUSHI_KOJIN_NO, RKIHON.SYS_KOSHINBI);
  --帳票基本データ(医療全体分)出力
  IF BRTN THEN
    BRTN := FUNC_FIRYINSERT;
  END IF;
  -- 賦課個人データ抽出
  IF BRTN THEN
      FOR CRKOJIN IN CKOJIN LOOP
        -- 帳票基本データ（個人）データ存在確認
        IF FUNC_FKOJINLSTGET(i_NMODE, RKIHON.KOKU_SETAI_NO, CRKOJIN.KOJIN_NO, RKIHON.SANTEIDANTAI_CD,
                           RKIHON.CHOTEI_NENDO, RKIHON.NENDO_BUN, RKIHON.R_NO, RKIHON.TSUCHI_NO) = 0 THEN
          --賦課個人データ項目編集
          BRTN := FUNC_FKOJINEDIT(i_NMODE, RKIHON.KOKU_SETAI_NO, CRKOJIN.KOJIN_NO, RKIHON.SANTEIDANTAI_CD,
                                   RKIHON.CHOTEI_NENDO, RKIHON.NENDO_BUN, RKIHON.R_NO, RKIHON.TSUCHI_NO);
        END IF;
      END LOOP;
  END IF;
  --終了
  RETURN(BRTN);
EXCEPTION
  WHEN OTHERS THEN
    --異常終了
    VLOG_LOG := SQLERRM || ' FUNC_FIRYEDIT ';
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END FUNC_FIRYEDIT;
