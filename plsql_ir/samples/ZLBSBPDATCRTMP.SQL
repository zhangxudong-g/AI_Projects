--2025/08/11 ZCZL.LIUCHENYU ADD 1.1.200.000:子ども子育て支援金対応
    ,KDM_ZEN_ZEIGAKU      ZLBTKIBETSU_MID.KDM_ZEN_ZEIGAKU%TYPE
  );
  MTCHOTEI      CHOTEI;
  --賦課期別抽出カーソル
  CURSOR CKIBETSU IS
    SELECT RONRIKIBETSU, NUSHI_KOJIN_NO, IR_ZEN_ZEIGAKU, IR_TAI_ZEIGAKU, KAI_ZEN_ZEIGAKU, KAI_TAI_ZEIGAKU
           ,SIEN_ZEN_ZEIGAKU, SIEN_TAI_ZEIGAKU
--2025/08/11 ZCZL.LIUCHENYU ADD 1.1.200.000:子ども子育て支援金対応
           ,KDM_ZEN_ZEIGAKU
      FROM ZLBTKIBETSU_MID  --賦課期別(確認用)
     WHERE KAMOKU_CD       = c_NGYOMUCD
       AND KAMOKUS_CD      = c_NGYOMUSCD
       AND KOKU_SETAI_NO   = i_NKOKUSETAINO
       AND SANTEIDANTAI_CD = i_NSANTEIDANTAICD
       AND CHOTEINENDO     = i_NCHOTEINENDO
       AND NENDOBUN        = i_NNENDOBUN
       AND TSUCHI_NO       = i_NTSUCHINO
       AND R_NO            = i_NRNO
    ORDER BY RONRIKIBETSU;
BEGIN
  --領域の初期化
  JLPPDATACLR;
  --カーソルの呼び出し
  FOR CREC IN CKIBETSU LOOP
    --データ取り出し
    MTCHOTEI := CREC;
    CASE i_NDATAMODE
      WHEN 1 THEN   --医療全体
        NKINGAK := MTCHOTEI.IR_ZEN_ZEIGAKU;
      WHEN 2 THEN   --医療退職
        NKINGAK := MTCHOTEI.IR_TAI_ZEIGAKU;
      WHEN 3 THEN   --介護全体
        NKINGAK := MTCHOTEI.KAI_ZEN_ZEIGAKU;
      WHEN 4 THEN   --介護退職
        NKINGAK := MTCHOTEI.KAI_TAI_ZEIGAKU;
      WHEN 5 THEN   --支援全体
        NKINGAK := MTCHOTEI.SIEN_ZEN_ZEIGAKU;
      WHEN 6 THEN   --支援退職
        NKINGAK := MTCHOTEI.SIEN_TAI_ZEIGAKU;
--2025/08/11 ZCZL.LIUCHENYU ADD START 1.1.200.000:子ども子育て支援金対応
      WHEN 7 THEN   --子ども全体税額
        NKINGAK := MTCHOTEI.KDM_ZEN_ZEIGAKU;
--2025/08/11 ZCZL.LIUCHENYU ADD END
      ELSE          --PGバグ以外ない
        NKINGAK := 0;
    END CASE;
    --論理期別毎に調定額振分
    JLPPSETCHOTEI(MTCHOTEI.RONRIKIBETSU, MTCHOTEI.NUSHI_KOJIN_NO, NKINGAK);
  END LOOP;
  --正常終了
  RETURN(TRUE);
EXCEPTION
  WHEN OTHERS THEN
    --異常終了
    VLOG_LOG := SQLERRM || ' FUNC_FGETCHOTEI ';
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END FUNC_FGETCHOTEI;
