-- 賦課所得取得
--------------------------------------------------------------------------------
FUNCTION FUNC_FSHOTOKUGET(i_NMODE         IN NUMBER, i_ICNT      IN PLS_INTEGER, i_ISENCNT     IN PLS_INTEGER, i_IJHOCNT IN PLS_INTEGER,
                        i_NKOKUSETAINO  IN NUMBER, i_NNUSHIKOJINNO  IN NUMBER, i_NSANTEIDANTAICD  IN NUMBER,
                        i_NCHOTEINENDO  IN NUMBER, i_NNENDOBUN      IN NUMBER, i_NRNO             IN NUMBER, i_NTSUCHINO    IN NUMBER
                       ) RETURN BOOLEAN IS
  BJOBUN    BOOLEAN;        --条文取得
  ICNT      PLS_INTEGER;    --設定件数
  IKANOCNT  PLS_INTEGER;    --設定可能件数
  VNAME     ZLBTKOJINLST_MID.SHOTOKU_NAME1%TYPE;
  -- 賦課所得取得用
  TYPE SHOTOKU IS RECORD
  (
    SHOTOKU_KBN           ZLBTSHOTOKU_MID.SHOTOKU_KBN%TYPE,
    SHOTOKU_KINGAKU       ZLBTSHOTOKU_MID.SHOTOKU_KINGAKU%TYPE,
    JOBUN                 ZLBTSHOTOKU_MID.JOBUN%TYPE
  );
  MTSHOTOKU      SHOTOKU;

  --賦課所得(最新)抽出カーソル
  CURSOR CSHOTOKU(p_NKOKUSETAINO IN NUMBER,      p_NNUSHIKOJINNO IN NUMBER,      p_NSANTEIDANTAI_CD  IN NUMBER,
                   p_ICHOTEINENDO IN PLS_INTEGER, p_INENDOBUN     IN PLS_INTEGER, p_NTSUCHINO         IN NUMBER) IS
    SELECT SHOTOKU_KBN, SHOTOKU_KINGAKU, JOBUN
      FROM ZLBTSHOTOKU_MID
     WHERE KOKU_SETAI_NO   = p_NKOKUSETAINO
       AND KOJIN_NO        = p_NNUSHIKOJINNO
       AND SANTEIDANTAI_CD = p_NSANTEIDANTAI_CD
       AND CHOTEI_NENDO    = p_ICHOTEINENDO
       AND NENDO_BUN       = p_INENDOBUN
       AND TSUCHI_NO       = p_NTSUCHINO
       AND SHOTOKU_KBN NOT IN (c_KYUYO_SHUNYU_IPN, c_KYUYO_SHUNYU_SENJU, c_KOTEKINEN_SHUNYU
                             , c_KOTEKINEN_KOUJYO)  --年金控除額も省く
     ORDER BY SHOTOKU_KBN;
BEGIN
  --項目初期化
  BJOBUN  := TRUE;
  ICNT    := 0;
  IKANOCNT := 0;
  VNAME    := '';
  --領域の初期化
  JLPPKOJINDATACLR;
  --設定可能件数
  CASE
    WHEN i_ISENCNT <> 0 AND i_IJHOCNT =  0  THEN
      IF i_ICNT <= 5 THEN
        IKANOCNT := 5;
      ELSE
        IKANOCNT := 4;
        RKOJINLST.SHOTOKU_NAME5 := 'その他';
      END IF;
    WHEN i_ISENCNT =  0 AND i_IJHOCNT <> 0  THEN
      IF i_ICNT <= 4 THEN
        IKANOCNT := 4;
      ELSE
        IKANOCNT := 3;
        RKOJINLST.SHOTOKU_NAME4 := 'その他';
      END IF;
    WHEN i_ISENCNT <> 0 AND i_IJHOCNT <> 0  THEN
      IF i_ICNT <= 3 THEN
        IKANOCNT := 3;
      ELSE
        IKANOCNT := 2;
        RKOJINLST.SHOTOKU_NAME3 := 'その他';
      END IF;
    WHEN i_ICNT    >  6 THEN
      IKANOCNT := 5;
      RKOJINLST.SHOTOKU_NAME6 := 'その他';
    ELSE
      IKANOCNT := 6;
  END CASE;

  --カーソルの呼び出し
  FOR MTSHOTOKU IN CSHOTOKU(i_NKOKUSETAINO, i_NNUSHIKOJINNO, i_NSANTEIDANTAICD, i_NCHOTEINENDO, i_NNENDOBUN, i_NTSUCHINO) LOOP         --賦課所得(最新)
    --データ取り出し
    CASE
      WHEN MTSHOTOKU.SHOTOKU_KBN BETWEEN 13 AND 17 THEN
        RKOJINLST.SHOTOKU_NAME5 := '譲渡特控前';
        RKOJINLST.SHOTOKU_GAKU5 := RKOJINLST.SHOTOKU_GAKU5 + MTSHOTOKU.SHOTOKU_KINGAKU;
        IF BJOBUN THEN
          BJOBUN  := FALSE;
          --条文編集
          VNAME := MTSHOTOKU.JOBUN;               --条文名
          IF  VNAME IS NULL THEN
            RKOJINLST.JOBUN_NAME := '';
          ELSE
            RKOJINLST.JOBUN_NAME := SUBSTR(VNAME, 1, 2);
            IF SUBSTR(VNAME, 3, 2) IS NOT NULL AND SUBSTR(VNAME, 3, 2) <> '  ' THEN
              IF RKOJINLST.JOBUN_NAME = '  ' THEN
                RKOJINLST.JOBUN_NAME := SUBSTR(VNAME, 3, 2);
              ELSE
                RKOJINLST.JOBUN_NAME := RKOJINLST.JOBUN_NAME || '-' || SUBSTR(VNAME, 3, 2);
              END IF;
              IF SUBSTR(VNAME, 5, 1) IS NOT NULL AND SUBSTR(VNAME, 5, 1) <> ' ' THEN
                RKOJINLST.JOBUN_NAME := RKOJINLST.JOBUN_NAME || '-' || SUBSTR(VNAME, 5, 1);
                IF SUBSTR(VNAME, 6, 1) IS NOT NULL AND SUBSTR(VNAME, 6, 1) <> ' ' THEN
                  RKOJINLST.JOBUN_NAME := RKOJINLST.JOBUN_NAME || '-' || SUBSTR(VNAME, 6, 1);
                  IF SUBSTR(VNAME, 7, 1) IS NOT NULL AND SUBSTR(VNAME, 7, 1) <> ' ' THEN
                    RKOJINLST.JOBUN_NAME := RKOJINLST.JOBUN_NAME || '-' || SUBSTR(VNAME, 7, 1);
                  END IF;
                END IF;
              END IF;
            END IF;
          END IF;
        END IF;
      WHEN MTSHOTOKU.SHOTOKU_KBN BETWEEN 41 AND 45 THEN
        RKOJINLST.SHOTOKU_NAME6 := '特別控除額';
        RKOJINLST.SHOTOKU_GAKU6 := RKOJINLST.SHOTOKU_GAKU6 + MTSHOTOKU.SHOTOKU_KINGAKU;
      WHEN MTSHOTOKU.SHOTOKU_KBN IN (51,52) THEN
        IF i_IJHOCNT = 0 THEN
          RKOJINLST.SHOTOKU_NAME6 := '専従控除';
          RKOJINLST.SHOTOKU_GAKU6 := RKOJINLST.SHOTOKU_GAKU6 + MTSHOTOKU.SHOTOKU_KINGAKU;
        ELSE
          RKOJINLST.SHOTOKU_NAME4 := '専従控除';
          RKOJINLST.SHOTOKU_GAKU4 := RKOJINLST.SHOTOKU_GAKU4 + MTSHOTOKU.SHOTOKU_KINGAKU;
        END IF;
      ELSE
        IF ICNT < IKANOCNT THEN
          VNAME := FUNC_FCODENAMEGET(3, MTSHOTOKU.SHOTOKU_KBN);  --所得名称取得
          IF VNAME IS NULL THEN
            VNAME := '';
          END IF;
        ELSE
          VNAME := '';
        END IF;
        ICNT := ICNT + 1;
        CASE
          WHEN ICNT = 1 THEN
            RKOJINLST.SHOTOKU_NAME1 := VNAME;
            RKOJINLST.SHOTOKU_GAKU1 := MTSHOTOKU.SHOTOKU_KINGAKU;
          WHEN ICNT = 2 THEN
            RKOJINLST.SHOTOKU_NAME2 := VNAME;
            RKOJINLST.SHOTOKU_GAKU2 := MTSHOTOKU.SHOTOKU_KINGAKU;
          WHEN ICNT = 3 OR (IKANOCNT = 2 AND ICNT > IKANOCNT) THEN
            IF ICNT <= IKANOCNT THEN
              RKOJINLST.SHOTOKU_NAME3 := VNAME;
            END IF;
            RKOJINLST.SHOTOKU_GAKU3 := RKOJINLST.SHOTOKU_GAKU3 + MTSHOTOKU.SHOTOKU_KINGAKU;
          WHEN ICNT = 4 OR (IKANOCNT = 3 AND ICNT > IKANOCNT) THEN
            IF ICNT <= IKANOCNT THEN
              RKOJINLST.SHOTOKU_NAME4 := VNAME;
            END IF;
            RKOJINLST.SHOTOKU_GAKU4 := RKOJINLST.SHOTOKU_GAKU4 + MTSHOTOKU.SHOTOKU_KINGAKU;
          WHEN ICNT = 5 OR (IKANOCNT = 4 AND ICNT > IKANOCNT) THEN
            IF ICNT <= IKANOCNT THEN
              RKOJINLST.SHOTOKU_NAME5 := VNAME;
            END IF;
            RKOJINLST.SHOTOKU_GAKU5 := RKOJINLST.SHOTOKU_GAKU5 + MTSHOTOKU.SHOTOKU_KINGAKU;
          WHEN ICNT = 6 OR (IKANOCNT = 5 AND ICNT > IKANOCNT) THEN
            IF ICNT <= IKANOCNT THEN
              RKOJINLST.SHOTOKU_NAME6 := VNAME;
            END IF;
            RKOJINLST.SHOTOKU_GAKU6 := RKOJINLST.SHOTOKU_GAKU6 + MTSHOTOKU.SHOTOKU_KINGAKU;
          ELSE
            RKOJINLST.SHOTOKU_NAME6 := VNAME;
        END CASE;
    END CASE;
  END LOOP;
  --正常終了
  RETURN(TRUE);
EXCEPTION
  WHEN OTHERS THEN
    --異常終了
    VLOG_LOG := SQLERRM || ' FUNC_FSHOTOKUGET ';
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END FUNC_FSHOTOKUGET;
--------------------------------------------------------------------------------
-- 賦課個人データ項目編集
--------------------------------------------------------------------------------
FUNCTION FUNC_FKOJINEDIT(i_NMODE          IN NUMBER, i_NKOKUSETAINO     IN NUMBER,
                       i_NNUSHIKOJINNO  IN NUMBER, i_NSANTEIDANTAICD  IN NUMBER,
                       i_NCHOTEINENDO   IN NUMBER, i_NNENDOBUN        IN NUMBER,
                       i_NRNO           IN NUMBER, i_NTSUCHINO        IN NUMBER
                      ) RETURN BOOLEAN IS
  BRTN      BOOLEAN := TRUE;--リターン値
  IRTN      PLS_INTEGER;    --リターン値
  ICNT      PLS_INTEGER;    --所得種類数
  ISENCNT   PLS_INTEGER;    --専従控除種類数
  IJHOCNT   PLS_INTEGER;    --譲渡所得種類数
  ITOKUCNT  PLS_INTEGER;    --特別控除種類数
  VSQL      VARCHAR2(256);  --SQL文
  VWHERE1   VARCHAR2(256);  --SQL文
  VWHERE2   VARCHAR2(256);  --SQL文

  RATENA_REC  GABTATENAKIHON%ROWTYPE;  --宛名基本
  RSOFU_REC   GABTSOFUSAKI%ROWTYPE;    --送付先
BEGIN
  --項目初期化
  ICNT      := 0;
  ISENCNT   := 0;
  IJHOCNT   := 0;
  ITOKUCNT  := 0;
  --賦課個人データ抽出
  VSQL :=                   ' SELECT *';
    VSQL := VSQL         || ' FROM ZLBTKOJIN_MID';
  VWHERE1 :=                ' WHERE KOKU_SETAI_NO   = ' || i_NKOKUSETAINO;
  VWHERE1 := VWHERE1     || ' AND KOJIN_NO          = ' || i_NNUSHIKOJINNO;
  VWHERE1 := VWHERE1     || ' AND SANTEIDANTAI_CD   = ' || i_NSANTEIDANTAICD;
  VWHERE1 := VWHERE1     || ' AND CHOTEI_NENDO      = ' || i_NCHOTEINENDO;
  VWHERE1 := VWHERE1     || ' AND NENDO_BUN         = ' || i_NNENDOBUN;
  VWHERE1 := VWHERE1     || ' AND TSUCHI_NO         = ' || i_NTSUCHINO;
  EXECUTE IMMEDIATE VSQL || VWHERE1 INTO RKOJIN;

  --所得種類数取得
  VSQL :=                   'SELECT COUNT(*)';
    VSQL := VSQL         || ' FROM ZLBTSHOTOKU_MID';
  VWHERE1 := VWHERE1 || ' AND SHOTOKU_KBN NOT IN (' || c_KYUYO_SHUNYU_IPN || ',' || c_KYUYO_SHUNYU_SENJU || ',' || c_KOTEKINEN_SHUNYU
                     || ',' || c_KOTEKINEN_KOUJYO || ') ';

  EXECUTE IMMEDIATE VSQL || VWHERE1 INTO ICNT;

  --専従控除種類数取得
  VWHERE2 :=                ' AND SHOTOKU_KBN IN (51, 52)';
  EXECUTE IMMEDIATE VSQL || VWHERE1 || VWHERE2 INTO ISENCNT;

  --譲渡所得種類数取得
  VWHERE2 :=                ' AND SHOTOKU_KBN BETWEEN 13 AND 17';
  EXECUTE IMMEDIATE VSQL || VWHERE1 || VWHERE2 INTO IJHOCNT;

  --特別控除種類数取得
  VWHERE2 :=                ' AND SHOTOKU_KBN BETWEEN 41 AND 45';
  EXECUTE IMMEDIATE VSQL || VWHERE1 || VWHERE2 INTO ITOKUCNT;

  --まとめる項目以外の項目数を求める
  ICNT := ICNT - ISENCNT - IJHOCNT;
  IF IJHOCNT > 0 THEN
    ICNT := ICNT - ITOKUCNT;
  END IF;

  --賦課所得取得
  BRTN := FUNC_FSHOTOKUGET(i_NMODE, ICNT, ISENCNT, IJHOCNT,
                         i_NKOKUSETAINO, i_NNUSHIKOJINNO, i_NSANTEIDANTAICD,
                         i_NCHOTEINENDO, i_NNENDOBUN, i_NRNO, i_NTSUCHINO);
  --個人宛名情報取得
  IRTN := GAAPK0030.FGETATENA(i_NNUSHIKOJINNO , c_VGYOMUCD, IKIJUNBI, RATENA_REC, RSOFU_REC);
  RIRYFUKALST.NUSHI_KANA_NAME   := RATENA_REC.SHIMEI_KANA;   --氏名かな
  RIRYFUKALST.NUSHI_NAME        := RATENA_REC.SHIMEI_KANJI;  --氏名漢字
  --帳票基本データ(個人)出力
  IF BRTN THEN
    BRTN := FUNC_FKOJININSERT;
  END IF;
  RETURN(BRTN);
EXCEPTION
  WHEN OTHERS THEN
  --異常終了
    VLOG_LOG := SQLERRM || ' FUNC_FKOJINEDIT ';
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END FUNC_FKOJINEDIT;
