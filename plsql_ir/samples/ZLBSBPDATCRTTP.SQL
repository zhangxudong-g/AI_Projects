      AND M.SANTEIDANTAI_CD = RKIHON.SANTEIDANTAI_CD
      AND M.CHOTEI_NENDO    = RKIHON.CHOTEI_NENDO
      AND M.NENDO_BUN       = RKIHON.NENDO_BUN
      AND M.TSUCHI_NO       = RKIHON.TSUCHI_NO;
    END IF;
  EXCEPTION
    WHEN OTHERS THEN
      --異常終了
      --バッチ処理ログ出力
      VLOG_LOG := SQLERRM || ' JLPFIRYINSERT 国保世帯番号=' || RKIHON.KOKU_SETAI_NO;
      PROCWRITELOG(c_NG, VLOG_LOG);
  END;

  IF NwCNT = 0 THEN
