FUNCTION FUNCMAIN RETURN BOOLEAN IS
  TYPE CURTYPE IS REF CURSOR;
  MKIHON      CURTYPE;
  BRTN        BOOLEAN := TRUE;
  VSQL        VARCHAR2(1024);
  NSETAI_NO   NUMBER;
  IDANTAI_CD  PLS_INTEGER;
  INENDO      PLS_INTEGER;
  INENDOBUN   PLS_INTEGER;
  NTSUCHI_NO  NUMBER;
  IKOSHINBI   PLS_INTEGER;
  IR_NO       PLS_INTEGER;
  IKBN        PLS_INTEGER;
BEGIN
  BEGIN

    -- 中間テーブルを対象とする。（対象者のみで今月末を作成する。）
    VSQL := 'SELECT KOKU_SETAI_NO, SANTEIDANTAI_CD, CHOTEI_NENDO, NENDO_BUN, TSUCHI_NO, SYS_KOSHINBI';
    VSQL := VSQL || ' FROM ZLBTKIHON_MID JL';
    IF ISTART_TEST <> 0 THEN
      --テストパターン使用時
      VSQL := VSQL || ', KKBTPATERN  AH';
      VSQL := VSQL || ' WHERE GYOMU_CD = ''' || c_VGYOMUCD || '''';  --業務コード（'ZLB'）
      VSQL := VSQL || ' AND GYOMU_SUBCD = 0';              --業務サブコード
      VSQL := VSQL || ' AND PATERN_NO   = 1';              --パターン番号（現状：１固定）
      VSQL := VSQL || ' AND KOKU_SETAI_NO = TO_NUMBER(KEY_NO)'; --キー項目（現状：国保世帯番号）
      VSQL := VSQL || ' AND CHOTEI_NENDO = ' || ITAISHONENDO;
    END IF;
    VSQL := VSQL || ' ORDER BY KOKU_SETAI_NO, SANTEIDANTAI_CD, CHOTEI_NENDO, NENDO_BUN, TSUCHI_NO';

    --賦課基本(最新)抽出カーソルオープン
    OPEN MKIHON FOR VSQL;
    LOOP
      --データ取り出し
      FETCH MKIHON INTO NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IKOSHINBI;
      EXIT WHEN MKIHON%NOTFOUND;

      --最新データの更新日が基準日より新しければ、履歴データを対象とする
      IF IKOSHINBI > IKIJUNBI2 THEN
        IKBN := 1;  --履歴データ処理
      ELSE
        IKBN := 0;  --最新データ処理
      END IF;

      IF BRTN THEN
        IR_NO := 0;
        --賦課基本から帳票基本データ(医療全体分)作成
        BRTN := JLPFKIHONNGET(IKBN, NSETAI_NO, IDANTAI_CD, INENDO, INENDOBUN, NTSUCHI_NO, IR_NO);
        EXIT WHEN BRTN = FALSE;
      END IF;
      COMMIT;
    END LOOP;
    CLOSE MKIHON;

    IF NOT BRTN THEN
      --エラー対象ログ出力
      VLOG_LOG := '処理対象 国保世帯番号=' || NSETAI_NO || ' 通知書番号=' || NTSUCHI_NO;
      VLOG_LOG := VLOG_LOG || ' 算定団体コード=' || IDANTAI_CD || ' 年度分=' || INENDOBUN;
      PROCWRITELOG(c_NG, VLOG_LOG);
    END IF;

  EXCEPTION
    WHEN OTHERS THEN
      --異常終了
      VLOG_LOG := SQLERRM || ' FUNCMAIN ';
      PROCWRITELOG(c_NG, VLOG_LOG);
  END;
  RETURN(BRTN);
END FUNCMAIN;
