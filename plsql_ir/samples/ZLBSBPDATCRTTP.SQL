-- 賦課個人データ項目編集
--------------------------------------------------------------------------------
FUNCTION JLPFKOJINEDIT(i_NMODE          IN NUMBER, i_NKOKUSETAINO     IN NUMBER,
                       i_NNUSHIKOJINNO  IN NUMBER, i_NSANTEIDANTAICD  IN NUMBER,
                       i_NCHOTEINENDO   IN NUMBER, i_NNENDOBUN        IN NUMBER,
                       i_NRNO           IN NUMBER, i_NTSUCHINO        IN NUMBER
                      ) RETURN BOOLEAN IS
  BRTN      BOOLEAN := TRUE;--リターン値
  IRTN      PLS_INTEGER;    --リターン値
  ICNT      PLS_INTEGER;    --所得種類数
  ISENCNT   PLS_INTEGER;    --専従控除種類数
  IJHOCNT   PLS_INTEGER;    --譲渡所得種類数
  ITOKUCNT  PLS_INTEGER;    --特別控除種類数
  VSQL      VARCHAR2(256);  --SQL文
  VWHERE1   VARCHAR2(256);  --SQL文
  VWHERE2   VARCHAR2(256);  --SQL文

  RATENA_REC  GABTATENAKIHON%ROWTYPE;  --宛名基本
  RSOFU_REC   GABTSOFUSAKI%ROWTYPE;    --送付先
BEGIN
  --項目初期化
  ICNT      := 0;
  ISENCNT   := 0;
  IJHOCNT   := 0;
  ITOKUCNT  := 0;
  --賦課個人データ抽出
  VSQL :=                   ' SELECT *';
  IF i_NMODE = 0 THEN
    VSQL := VSQL         || ' FROM ZLBTKOJIN_N';
  ELSE
    VSQL := VSQL         || ' FROM ZLBTKOJIN_R';
  END IF;
  VWHERE1 :=                ' WHERE KOKU_SETAI_NO   = ' || i_NKOKUSETAINO;
  VWHERE1 := VWHERE1     || ' AND KOJIN_NO          = ' || i_NNUSHIKOJINNO;
  VWHERE1 := VWHERE1     || ' AND SANTEIDANTAI_CD   = ' || i_NSANTEIDANTAICD;
  VWHERE1 := VWHERE1     || ' AND CHOTEI_NENDO      = ' || i_NCHOTEINENDO;
  VWHERE1 := VWHERE1     || ' AND NENDO_BUN         = ' || i_NNENDOBUN;
  IF i_NMODE = 1 THEN
    VWHERE1 := VWHERE1   || ' AND R_NO              = ' || i_NRNO;
  END IF;
  VWHERE1 := VWHERE1     || ' AND TSUCHI_NO         = ' || i_NTSUCHINO;
  EXECUTE IMMEDIATE VSQL || VWHERE1 INTO RKOJIN;

  --所得種類数取得
  VSQL :=                   'SELECT COUNT(*)';
  IF i_NMODE = 0 THEN
    VSQL := VSQL         || ' FROM ZLBTSHOTOKU_N';
  ELSE
    VSQL := VSQL         || ' FROM ZLBTSHOTOKU_R';
  END IF;
  VWHERE1 := VWHERE1 || ' AND SHOTOKU_KBN NOT IN (' || c_KYUYO_SHUNYU_IPN || ',' || c_KYUYO_SHUNYU_SENJU || ',' || c_KOTEKINEN_SHUNYU
                     || ',' || c_KOTEKINEN_KOUJYO || ') ';

  EXECUTE IMMEDIATE VSQL || VWHERE1 INTO ICNT;

  --専従控除種類数取得
  VWHERE2 :=                ' AND SHOTOKU_KBN IN (51, 52)';
  EXECUTE IMMEDIATE VSQL || VWHERE1 || VWHERE2 INTO ISENCNT;

  --譲渡所得種類数取得
  VWHERE2 :=                ' AND SHOTOKU_KBN BETWEEN 13 AND 17';
  EXECUTE IMMEDIATE VSQL || VWHERE1 || VWHERE2 INTO IJHOCNT;

  --特別控除種類数取得
  VWHERE2 :=                ' AND SHOTOKU_KBN BETWEEN 41 AND 45';
  EXECUTE IMMEDIATE VSQL || VWHERE1 || VWHERE2 INTO ITOKUCNT;

  --まとめる項目以外の項目数を求める
  ICNT := ICNT - ISENCNT - IJHOCNT;
  IF IJHOCNT > 0 THEN
    ICNT := ICNT - ITOKUCNT;
  END IF;

  --賦課所得取得
  BRTN := JLPFSHOTOKUGET(i_NMODE, ICNT, ISENCNT, IJHOCNT,
                         i_NKOKUSETAINO, i_NNUSHIKOJINNO, i_NSANTEIDANTAICD,
                         i_NCHOTEINENDO, i_NNENDOBUN, i_NRNO, i_NTSUCHINO);
  --個人宛名情報取得
  IRTN := GAAPK0030.FGETATENA(i_NNUSHIKOJINNO , c_VGYOMUCD, IKIJUNBI, RATENA_REC, RSOFU_REC);
  RIRYFUKALST.NUSHI_KANA_NAME   := RATENA_REC.SHIMEI_KANA;   --氏名かな
  RIRYFUKALST.NUSHI_NAME        := RATENA_REC.SHIMEI_KANJI;  --氏名漢字
  --帳票基本データ(個人)出力
  IF BRTN THEN
    BRTN := JLPFKOJININSERT;
  END IF;
  RETURN(BRTN);
EXCEPTION
  WHEN OTHERS THEN
  --異常終了
    VLOG_LOG := SQLERRM || ' JLPFKOJINEDIT ';
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END JLPFKOJINEDIT;
