-- 調定額取得
--------------------------------------------------------------------------------
FUNCTION JLPFGETCHOTEI(i_NMODE          IN NUMBER, i_NDATAMODE IN NUMBER, i_NKOKUSETAINO  IN NUMBER, i_NSANTEIDANTAICD  IN NUMBER,
                       i_NCHOTEINENDO   IN NUMBER, i_NNENDOBUN IN NUMBER, i_NTSUCHINO     IN NUMBER, i_NRNO             IN NUMBER
                      ) RETURN BOOLEAN IS
  NKINGAK       NUMBER;                     --取得金額
  -- 調定額取得用
  TYPE CHOTEI IS RECORD
  (
    RONRIKIBETSU          ZLBTKIBETSU_N.RONRIKIBETSU%TYPE,
    NUSHI_KOJIN_NO        ZLBTKIBETSU_N.NUSHI_KOJIN_NO%TYPE,
    IR_ZEN_ZEIGAKU        ZLBTKIBETSU_N.IR_ZEN_ZEIGAKU%TYPE,
    IR_TAI_ZEIGAKU        ZLBTKIBETSU_N.IR_TAI_ZEIGAKU%TYPE,
    KAI_ZEN_ZEIGAKU       ZLBTKIBETSU_N.KAI_ZEN_ZEIGAKU%TYPE,
    KAI_TAI_ZEIGAKU       ZLBTKIBETSU_N.KAI_TAI_ZEIGAKU%TYPE
  );
  MTCHOTEI      CHOTEI;
  --賦課期別抽出カーソル
  CURSOR CKIBETSU IS
    SELECT RONRIKIBETSU, NUSHI_KOJIN_NO, IR_ZEN_ZEIGAKU, IR_TAI_ZEIGAKU, KAI_ZEN_ZEIGAKU, KAI_TAI_ZEIGAKU
      FROM ZLBTKIBETSU_N  --賦課期別(最新)
     WHERE KAMOKU_CD       = c_NGYOMUCD
       AND KAMOKUS_CD      = c_NGYOMUSCD
       AND KOKU_SETAI_NO   = i_NKOKUSETAINO
       AND SANTEIDANTAI_CD = i_NSANTEIDANTAICD
       AND CHOTEINENDO     = i_NCHOTEINENDO
       AND NENDOBUN        = i_NNENDOBUN
       AND TSUCHI_NO       = i_NTSUCHINO
       AND R_NO            = i_NRNO
    UNION ALL
    SELECT RONRIKIBETSU, NUSHI_KOJIN_NO, IR_ZEN_ZEIGAKU, IR_TAI_ZEIGAKU, KAI_ZEN_ZEIGAKU, KAI_TAI_ZEIGAKU
      FROM ZLBTKIBETSU_R  --賦課期別(履歴)
     WHERE KAMOKU_CD       = c_NGYOMUCD
       AND KAMOKUS_CD      = c_NGYOMUSCD
       AND KOKU_SETAI_NO   = i_NKOKUSETAINO
       AND SANTEIDANTAI_CD = i_NSANTEIDANTAICD
       AND CHOTEINENDO     = i_NCHOTEINENDO
       AND NENDOBUN        = i_NNENDOBUN
       AND TSUCHI_NO       = i_NTSUCHINO
       AND R_NO            = i_NRNO
    ORDER BY RONRIKIBETSU;
BEGIN
  --領域の初期化
  PROC_PDATACLR;
  --カーソルの呼び出し
  FOR CREC IN CKIBETSU LOOP
    --データ取り出し
    MTCHOTEI := CREC;
    CASE i_NDATAMODE
      WHEN 1 THEN   --医療全体
        NKINGAK := MTCHOTEI.IR_ZEN_ZEIGAKU;
      WHEN 2 THEN   --医療退職
        NKINGAK := MTCHOTEI.IR_TAI_ZEIGAKU;
      WHEN 3 THEN   --介護全体
        NKINGAK := MTCHOTEI.KAI_ZEN_ZEIGAKU;
      WHEN 4 THEN   --介護退職
        NKINGAK := MTCHOTEI.KAI_TAI_ZEIGAKU;
      ELSE          --PGバグ以外ない
        NKINGAK := 0;
    END CASE;
    --論理期別毎に調定額振分
    PROC_PSETCHOTEI(MTCHOTEI.RONRIKIBETSU, MTCHOTEI.NUSHI_KOJIN_NO, NKINGAK);
  END LOOP;
  --正常終了
  RETURN(TRUE);
EXCEPTION
  WHEN OTHERS THEN
    --異常終了
    VLOG_LOG := SQLERRM || ' JLPFGETCHOTEI ';
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END JLPFGETCHOTEI;
