  CURSOR CKAITAI IS
    SELECT *
      FROM ZLBTKAI_TAI_N  --賦課介護退職(最新)
     WHERE KOKU_SETAI_NO   = i_NSETAI_NO
       AND SANTEIDANTAI_CD = i_IDANTAI_CD
       AND CHOTEI_NENDO    = i_INENDO
       AND NENDO_BUN       = i_INENDOBUN
       AND TSUCHI_NO       = i_NTSUCHI_NO
       AND R_NO            = i_R_NO
    UNION ALL
    SELECT *
      FROM ZLBTKAI_TAI_R  --賦課介護退職(履歴)
     WHERE KOKU_SETAI_NO   = i_NSETAI_NO
       AND SANTEIDANTAI_CD = i_IDANTAI_CD
