          ELSE
              BRTN := KKBPK5551.FSETBLOG (i_NRENBAN, 'ZLBSBPDATCRTTP', 1, '', '', SUBSTR(i_VMSG, 1, 256), i_NSHOKUIN_NO, i_VTANMATSU_NO);
