      INSERT INTO ZLBTIRY_PASSLST_TOU  VALUES RIRYFUKALST;
    ELSE
      --賦課基本出力
      INSERT INTO ZLBTIRY_PASSLST_ZEN VALUES RIRYFUKALST;
    END IF;
  END IF;

  --帳票基本データ(医療全体分)出力件数加算
  ICNT_O_IRY_FUKALST := ICNT_O_IRY_FUKALST + 1;
  --正常終了
  RETURN(TRUE);
EXCEPTION
  WHEN OTHERS THEN
    --異常終了
    --バッチ処理ログ出力
    VLOG_LOG := SQLERRM || ' JLPFIRYINSERT 国保世帯番号=' || RKIHON.KOKU_SETAI_NO;
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END JLPFIRYINSERT;
