                     i_R_NO IN PLS_INTEGER) RETURN BOOLEAN IS
  BRTN  BOOLEAN := TRUE;     --リターン値
  --賦課退職抽出カーソル
  CURSOR CTAI IS
    SELECT *
      FROM ZLBTTAI_N  --賦課退職(最新)
     WHERE KOKU_SETAI_NO   = i_NSETAI_NO
       AND SANTEIDANTAI_CD = i_IDANTAI_CD
       AND CHOTEI_NENDO    = i_INENDO
       AND NENDO_BUN       = i_INENDOBUN
       AND TSUCHI_NO       = i_NTSUCHI_NO
       AND R_NO            = i_R_NO
    UNION ALL
    SELECT *
      FROM ZLBTTAI_R  --賦課退職(履歴)
     WHERE KOKU_SETAI_NO   = i_NSETAI_NO
       AND SANTEIDANTAI_CD = i_IDANTAI_CD
       AND CHOTEI_NENDO    = i_INENDO
       AND NENDO_BUN       = i_INENDOBUN
       AND R_NO            = i_R_NO
       AND TSUCHI_NO       = i_NTSUCHI_NO
    ;
BEGIN
  RTAI := NULL;
  --賦課退職抽出カーソル呼び出し
  FOR CREC IN CTAI LOOP
    RTAI := CREC;
    --賦課退職読込件数加算
    ICNT_I_TAI := ICNT_I_TAI + 1;
    BRTN := JLPFIRYTAIEDIT(i_IKBN);
    EXIT;
  END LOOP;

  RETURN(BRTN);
EXCEPTION
  WHEN OTHERS THEN
    --異常終了
    VLOG_LOG := SQLERRM || ' JLPFTAINGET ';
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END JLPFTAINGET;
