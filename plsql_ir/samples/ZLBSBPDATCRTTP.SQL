-- 賦課基本データ抽出
--------------------------------------------------------------------------------
FUNCTION JLPFKIHONNGET(i_IKBN IN PLS_INTEGER, i_NSETAI_NO IN NUMBER, i_IDANTAI_CD IN PLS_INTEGER,
                       i_INENDO IN PLS_INTEGER, i_INENDOBUN IN PLS_INTEGER, i_NTSUCHI_NO IN NUMBER,
                       o_R_NO OUT PLS_INTEGER) RETURN BOOLEAN IS
  BRTN  BOOLEAN := FALSE;     --リターン値
  --賦課基本(最新)抽出カーソル 
  CURSOR CKIHONN IS
    SELECT *
      FROM ZLBTKIHON_N
      WHERE KOKU_SETAI_NO   = i_NSETAI_NO
        AND SANTEIDANTAI_CD = i_IDANTAI_CD
        AND NENDO_BUN       = i_INENDOBUN
      ORDER BY KOKU_SETAI_NO, SANTEIDANTAI_CD, CHOTEI_NENDO, NENDO_BUN, TSUCHI_NO;
  --賦課基本(履歴)抽出カーソル
  CURSOR CKIHONR IS
    SELECT *
      FROM ZLBTKIHON_R
      WHERE KOKU_SETAI_NO   = i_NSETAI_NO
        AND SANTEIDANTAI_CD = i_IDANTAI_CD
        AND NENDO_BUN       = i_INENDOBUN
        AND SYS_KOSHINBI   <= IKIJUNBI2
     ORDER BY KOKU_SETAI_NO, SANTEIDANTAI_CD, CHOTEI_NENDO, NENDO_BUN, R_NO DESC, TSUCHI_NO;
BEGIN
  RKIHON := NULL;
  o_R_NO := 0;
  --賦課基本抽出カーソル呼び出し
  IF i_IKBN = 0 THEN
    FOR CREC IN CKIHONN LOOP
      RKIHON := CREC;

    o_R_NO := RKIHON.R_NO;
    --賦課基本読込件数加算
    ICNT_I_KIHON := ICNT_I_KIHON + 1;
    BRTN := JLPFIRYEDIT(i_IKBN);
    EXIT WHEN BRTN = FALSE;

        --戻り値の履歴番号が０：データなしの場合、読み飛ばし
        IF RKIHON .R_NO > 0 THEN
          --賦課退職から帳票基本データ(医療退職分)作成
          BRTN := JLPFTAINGET(i_IKBN, i_NSETAI_NO, i_IDANTAI_CD, RKIHON.CHOTEI_NENDO, i_INENDOBUN, RKIHON.TSUCHI_NO, RKIHON.R_NO);
          EXIT WHEN BRTN = FALSE;

          --賦課介護基本から帳票基本データ(介護全体分)作成
          BRTN := JLPFKAIKIHONNGET(i_IKBN, i_NSETAI_NO, i_IDANTAI_CD, RKIHON.CHOTEI_NENDO, i_INENDOBUN, RKIHON.TSUCHI_NO, RKIHON.R_NO);
          EXIT WHEN BRTN = FALSE;

          --賦課介護退職から帳票基本データ(介護退職分)作成
          BRTN := JLPFKAITAINGET(i_IKBN, i_NSETAI_NO, i_IDANTAI_CD, RKIHON.CHOTEI_NENDO, i_INENDOBUN, RKIHON.TSUCHI_NO, RKIHON.R_NO);
          EXIT WHEN BRTN = FALSE;
        END IF;

    END LOOP;
  ELSE
    FOR CREC IN CKIHONR LOOP
      RKIHON := CREC;

    o_R_NO := RKIHON.R_NO;
    --賦課基本読込件数加算
    ICNT_I_KIHON := ICNT_I_KIHON + 1;
    BRTN := JLPFIRYEDIT(i_IKBN);
    EXIT WHEN BRTN = FALSE;

        --戻り値の履歴番号が０：データなしの場合、読み飛ばし
        IF RKIHON .R_NO > 0 THEN
          --賦課退職から帳票基本データ(医療退職分)作成
          BRTN := JLPFTAINGET(i_IKBN, i_NSETAI_NO, i_IDANTAI_CD, RKIHON.CHOTEI_NENDO, i_INENDOBUN, RKIHON.TSUCHI_NO, RKIHON.R_NO);
          EXIT WHEN BRTN = FALSE;

          --賦課介護基本から帳票基本データ(介護全体分)作成
          BRTN := JLPFKAIKIHONNGET(i_IKBN, i_NSETAI_NO, i_IDANTAI_CD, RKIHON.CHOTEI_NENDO, i_INENDOBUN, RKIHON.TSUCHI_NO, RKIHON.R_NO);
          EXIT WHEN BRTN = FALSE;

          --賦課介護退職から帳票基本データ(介護退職分)作成
          BRTN := JLPFKAITAINGET(i_IKBN, i_NSETAI_NO, i_IDANTAI_CD, RKIHON.CHOTEI_NENDO, i_INENDOBUN, RKIHON.TSUCHI_NO, RKIHON.R_NO);
          EXIT WHEN BRTN = FALSE;
        END IF;

    END LOOP;
  END IF;

  IF NOT BRTN THEN
    --データなし
    BRTN := TRUE;
  END IF;
  RETURN(BRTN);
EXCEPTION
  WHEN OTHERS THEN
    --異常終了
    VLOG_LOG := SQLERRM || ' JLPFKIHONNGET ';
    VLOG_LOG := VLOG_LOG || ' 国保世帯番号=' || i_NSETAI_NO || ' 通知書番号=' || i_NTSUCHI_NO;
    VLOG_LOG := VLOG_LOG || ' 算定団体コード=' || i_IDANTAI_CD || ' 年度分=' || i_INENDOBUN;
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END JLPFKIHONNGET;
