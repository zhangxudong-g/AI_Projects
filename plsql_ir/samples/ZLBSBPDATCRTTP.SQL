FUNCTION FUNCPARACHK RETURN BOOLEAN IS
  BRTN              BOOLEAN := TRUE; --リターン値
  io_WAREKIHIZUKE   PLS_INTEGER;
  io_SEIREKIHIZUKE  PLS_INTEGER;
  o_EDIT_GYYMMDD1   NVARCHAR2(9);
  o_EDIT_GYYMMDD2   NVARCHAR2(9);
  o_EDIT_GYYMMDD3   NVARCHAR2(11);
  o_KJ_GENGO        NVARCHAR2(2);
  o_KG_GENGO        VARCHAR2(1);
  o_WAREKI_YY       PLS_INTEGER;
  o_SEIREKI_YYYY    PLS_INTEGER;
  o_WAREKI_GYYMM    PLS_INTEGER;
  o_MM              PLS_INTEGER;
  o_DD              PLS_INTEGER;
  o_WAREKIHIZUKE    PLS_INTEGER;
BEGIN
  --対象年度チェック
  IF VSTART_JYOUKEN01 IS NULL THEN
    BRTN := FALSE;
  ELSIF LENGTH(VSTART_JYOUKEN01) <> 4 THEN
    BRTN := FALSE;
  END IF;

  --対象年度を'YYYY'形式に変換
  IF BRTN THEN
--西暦で取得するため削除
    ITAISHONENDO := TO_NUMBER(VSTART_JYOUKEN01);
    IF ITAISHONENDO = 0 THEN
      BRTN := FALSE;
    END IF;
  END IF;
  IF BRTN THEN
    --基準日チェック
    IF NVL(VSTART_JYOUKEN02, 0) = 0 THEN
      IKIJUNBI  := KKBPK5551.FBATCHDATEGET;   --システム日付取得
      IKIJUNBI2 := TO_NUMBER(TO_CHAR(SYSDATE, 'YYYYMMDD'));
    ELSE
      IKIJUNBI := TO_NUMBER(VSTART_JYOUKEN02);
      IKIJUNBI2 := IKIJUNBI;
    END IF;
  END IF;
  IF BRTN THEN
    --出力モードチェック
    IF VSTART_JYOUKEN03 IS NULL THEN
      BRTN := FALSE;
    ELSE
      IOUTMODE  := TO_NUMBER(VSTART_JYOUKEN03);
      --出力モードは、1:IRY_FUKALST等か、2:IRY_FUKALST1等であること。
      BRTN := (IOUTMODE = 1 OR IOUTMODE = 2);
    END IF;
  END IF;

  ISTART_TEST := 0; --テスト処理区分
  RETURN(BRTN);
EXCEPTION
  WHEN OTHERS THEN
    --異常終了
    VLOG_LOG := SQLERRM || ' FUNCPARACHK ';
    PROCWRITELOG(c_NG, VLOG_LOG);
    RETURN(FALSE);
END FUNCPARACHK;
