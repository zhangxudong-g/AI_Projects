FUNCTION JLPFKAITAINGET(i_IKBN IN PLS_INTEGER, i_NSETAI_NO IN NUMBER, i_IDANTAI_CD IN PLS_INTEGER,
                        i_INENDO IN PLS_INTEGER, i_INENDOBUN IN PLS_INTEGER, i_NTSUCHI_NO IN NUMBER,
