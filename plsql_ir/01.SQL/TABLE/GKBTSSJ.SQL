/
COMMENT ON COLUMN GKBTSSJ.UKETSUKENUM IS '受付番号'
/
COMMENT ON COLUMN GKBTSSJ.INSDATETIME IS '登録日時'
/
COMMENT ON COLUMN GKBTSSJ.SHINSEIKANRIJOKYOCODE IS '申請管理状況コード'
/
COMMENT ON COLUMN GKBTSSJ.RENRAKUJIKO IS '連絡事項'
/
COMMENT ON COLUMN GKBTSSJ.SHINSEISHORIINSDATETIME IS '申請処理状況登録日時'
/
COMMENT ON COLUMN GKBTSSJ.SOUSHINZUMIFLG IS '送信済フラグ'
/
COMMENT ON COLUMN GKBTSSJ.TETSUZUKICODE IS '手続きコード'
/
