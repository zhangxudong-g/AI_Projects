CREATE TABLE GKBTSSJ
(
    SHINSEIKANRISEQ                NUMBER(12,0) DEFAULT 0 NOT NULL,
    SHINSEISHORIJOKYOSEQ           NUMBER(2,0) DEFAULT 0 NOT NULL,
    UKETSUKENUM                    NVARCHAR2(15),
    INSDATETIME                    NUMBER(14,0),
    SHINSEIKANRIJOKYOCODE          NUMBER(3,0),
    RENRAKUJIKO                    CLOB,
    SHINSEISHORIINSDATETIME        TIMESTAMP(6),
    SOUSHINZUMIFLG                 NUMBER(1,0),
    TETSUZUKICODE                  CHAR(54),
    DELFLG                         NUMBER(1,0),
    SYS_SAKUSEIBI                  NUMBER(8,0),
    SYS_KOUSHINBI                  NUMBER(8,0),
    SYS_JIKAN                      NUMBER(6,0),
    SYS_SHOKUINKOJIN_NO            CHAR(12),
    SYS_TANMATU_NO                 NVARCHAR2(63),
    CONSTRAINT GKBTSSJ_PKEY PRIMARY KEY (SHINSEIKANRISEQ, SHINSEISHORIJOKYOSEQ) USING INDEX
        TABLESPACE GK_INDEX
        LOGGING
        ENABLE
)
TABLESPACE GK_DATA
NOCACHE
LOGGING
/
